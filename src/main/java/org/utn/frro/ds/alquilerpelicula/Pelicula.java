package org.utn.frro.ds.alquilerpelicula;

public class Pelicula {

	String codigo;
	String titulo;
	String genero;
	String descripcion;
	int duracion;
	
	public String getTitulo() {
		return titulo;
	}

	public String getCodigo() {
		return codigo;
	}

	public int getDuracion() {
		return duracion;
	}

	public String getGenero() {
		return genero;
	}

	public String getDescripcion() {
		return descripcion;
	}

}

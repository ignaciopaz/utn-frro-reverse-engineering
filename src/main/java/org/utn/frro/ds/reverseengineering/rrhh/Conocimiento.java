package org.utn.frro.ds.reverseengineering.rrhh;

public class Conocimiento {
	private String codConocimiento;
	private String descripcion;
	
	public String getCodConocimiento() {
		return codConocimiento;
	}
	public void setCodConocimiento(String pCodConocimiento) {
		codConocimiento = pCodConocimiento;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String pDescripcion) {
		descripcion = pDescripcion;
	}
}
